BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "Customer" (
	"CusID"	INTEGER NOT NULL UNIQUE,
	"FName"	TEXT,
	"LName"	TEXT,
	"IDNum"	TEXT,
	"IDState"	TEXT,
	"LicensePlate"	TEXT,
	"Address"	TEXT,
	"Phone"	TEXT,
	"Email"	TEXT,
	PRIMARY KEY("CusID" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "RoomTypes" (
	"Type"	INTEGER UNIQUE,
	"Rate"	REAL,
	PRIMARY KEY("Type")
);
CREATE TABLE IF NOT EXISTS "Reservations" (
	"ResID"	INTEGER NOT NULL UNIQUE,
	"DateMade"	TEXT,
	"CheckIn"	TEXT,
	"Checkout"	TEXT,
	"RoomType"	INTEGER,
	"CusID"	INTEGER,
	FOREIGN KEY("CusID") REFERENCES "Customer"("CusID"),
	FOREIGN KEY("RoomType") REFERENCES "RoomTypes"("Type"),
	PRIMARY KEY("ResID" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "Housekeeping" (
	"RoomID"	NUMERIC NOT NULL UNIQUE,
	"Keeper"	TEXT,
	"Bathroom"	INTEGER,
	"Towels"	INTEGER,
	"Vacuum"	INTEGER,
	"Dusting"	INTEGER,
	"Electronics"	INTEGER,
	FOREIGN KEY("RoomID") REFERENCES "Rooms"("RoomID"),
	PRIMARY KEY("RoomID")
);
CREATE TABLE IF NOT EXISTS "Payments" (
	"CusID"	INTEGER,
	"Payments"	REAL,
	"Balance"	REAL,
	FOREIGN KEY("CusID") REFERENCES "Customer"("CusID")
);
CREATE TABLE IF NOT EXISTS "Rooms" (
	"RoomID"	INTEGER UNIQUE,
	"Type"	INTEGER,
	"Status"	INTEGER,
	FOREIGN KEY("Type") REFERENCES "RoomTypes"("Type"),
	PRIMARY KEY("RoomID")
);
